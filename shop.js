shopBtn.onclick = function () {
    if (shopDiv.style.display = "none") {
        shopDiv.style.display = "block";
    }
    else if (shopDiv.style.display = "block") {
        shopDiv.style.display = "none";
        shopDiv.style.display = "none";
        shopDiv.style.display = "none";
        shopDiv.style.display = "none";
    };
};
